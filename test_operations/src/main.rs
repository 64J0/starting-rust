fn main() {
    // addition
    let sum: u8 = 5 + 8;
    println!("The value of sum is {}", sum);

    // division
    let quotient: f64 = 56.7 / 32.2;
    println!("The value of quotient is {}", quotient);

    // remainder
    let remainder: u8 = 43 % 5;
    println!("The value of remainder is {}", remainder);
}
